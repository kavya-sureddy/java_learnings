package java_learnings;

public class Grade {

	public static void main(String[] args) {
		int marks =20;
		if (marks>=90)
		{
		 System.out.println("Grade : A+ ");
		 
	    }
		else if (marks>=75)
		{
		System.out.println("Grade : A ");
		}
		else if (marks>=35)
		{
			System.out.println("Grade : B ");
		}
		else
			System.out.println("Grade : Fail ");
			
	}

}
