package java_learnings;

public class NestedFor {

	public static void main(String[] args) {
		
		System.out.println("----------------Tables of 11 t0 15----------------");
		for(int i=11; i<=15; i++) 
		{
			System.out.println("Tables of:" +i);
			
		for(int j=1; j<=10; j++)
		{
			System.out.println((i*j)+" ");
		}
		}
		}
	}
		


