package Javaoperates;

public class op6BItwise {

	public static void main(String[] args) {
		
//		| Operator | Meaning |    |
//		| -------- | ------- | -- |
//		| `&`      | AND     |    |
//		| `^`      | XOR     |    |

		int a = 5;
		int b = 3;

		System.out.println(a & b);
		System.out.println(a ^ b);
	}

}


